void SIM_HAL_SCGC5_SetClock(uint8_t SCGC5_feild)
{
  /* Set bit of field, enable clock */
  SIM->SCGC5 |= ((uint32_t)1U << SCGC5_feild);
}

void SIM_HAL_SCGC5_ResetClock(uint8_t SCGC5_feild)
{
  /* Reset bit of field, disable clock */
  SIM->SCGC5 &= ~((uint32_t)1U << SCGC5_feild);
}
